#[derive(Copy, Clone, PartialEq, Eq, Debug, Hash, Serialize, Deserialize)]
#[repr(u32)]
pub enum Language {
    English = 0x0,
    French = 0x1,
    Japanese = 0x2,
    German = 0x3,
    Spanish = 0x4,
    Italian = 0x5,
    Korean = 0x6,
    TradChinese = 0x7,
    Portuguese = 0x8,
    SimplChinese = 0x9,
    Russian = 0xB,
    Dutch = 0xC,
    Danish = 0xD,
    Norwegian = 0xE,
    Swedish = 0xF,
    Finnish = 0x10,
    GavChinese = 0x16,
    DevReference = 0x17,
}

impl TryFrom<u32> for Language {
    type Error = anyhow::Error;

    fn try_from(value: u32) -> Result<Self, Self::Error> {
        match value {
            0x0 => Ok(Self::English),
            0x1 => Ok(Self::French),
            0x2 => Ok(Self::Japanese),
            0x3 => Ok(Self::German),
            0x4 => Ok(Self::Spanish),
            0x5 => Ok(Self::Italian),
            0x6 => Ok(Self::Korean),
            0x7 => Ok(Self::TradChinese),
            0x8 => Ok(Self::Portuguese),
            0x9 => Ok(Self::SimplChinese),
            0xB => Ok(Self::Russian),
            0xC => Ok(Self::Dutch),
            0xD => Ok(Self::Danish),
            0xE => Ok(Self::Norwegian),
            0xF => Ok(Self::Swedish),
            0x10 => Ok(Self::Finnish),
            0x16 => Ok(Self::GavChinese),
            0x17 => Ok(Self::DevReference),
            _ => Err(anyhow!("Unknown language! {value:x}")),
        }
    }
}

impl From<Language> for u32 {
    #[allow(clippy::as_conversions)]
    fn from(value: Language) -> Self {
        value as Self
    }
}

