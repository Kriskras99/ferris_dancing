//! Contains the types that describe the usefull information in this filetype

use std::{borrow::Cow, collections::HashMap};

use serde::{Deserialize, Serialize};

use crate::utils::{errors::ParserError, LocaleId};

#[derive(Copy, Clone, PartialEq, Eq, Debug, Hash, Serialize, Deserialize)]
#[repr(u32)]
pub enum Language {
    English = 0x00,
    French = 0x01,
    Japanese = 0x02,
    German = 0x03,
    Spanish = 0x04,
    Italian = 0x05,
    Korean = 0x06,
    TradChinese = 0x07,
    Portuguese = 0x08,
    SimplChinese = 0x09,
    Russian = 0x0B,
    Dutch = 0x0C,
    Danish = 0x0D,
    Norwegian = 0x0E,
    Swedish = 0x0F,
    Finnish = 0x10,
    GavChinese = 0x16,
    DevReference = 0x17,
}

impl TryFrom<u32> for Language {
    type Error = ParserError;

    fn try_from(value: u32) -> Result<Self, Self::Error> {
        match value {
            0x0 => Ok(Self::English),
            0x1 => Ok(Self::French),
            0x2 => Ok(Self::Japanese),
            0x3 => Ok(Self::German),
            0x4 => Ok(Self::Spanish),
            0x5 => Ok(Self::Italian),
            0x6 => Ok(Self::Korean),
            0x7 => Ok(Self::TradChinese),
            0x8 => Ok(Self::Portuguese),
            0x9 => Ok(Self::SimplChinese),
            0xB => Ok(Self::Russian),
            0xC => Ok(Self::Dutch),
            0xD => Ok(Self::Danish),
            0xE => Ok(Self::Norwegian),
            0xF => Ok(Self::Swedish),
            0x10 => Ok(Self::Finnish),
            0x12 | 0x17 => Ok(Self::DevReference),
            0x16 => Ok(Self::GavChinese),
            _ => Err(ParserError::custom(format!(
                "Unknown language: 0x{value:x}"
            ))),
        }
    }
}

impl From<Language> for u32 {
    #[allow(
        clippy::as_conversions,
        reason = "Language is repr(u32) thus this is safe"
    )]
    fn from(value: Language) -> Self {
        value as Self
    }
}

impl Language {
    const ALL: &'static [Self] = &[
        Self::English,
        Self::French,
        Self::Japanese,
        Self::German,
        Self::Spanish,
        Self::Italian,
        Self::Korean,
        Self::TradChinese,
        Self::Portuguese,
        Self::SimplChinese,
        Self::Russian,
        Self::Dutch,
        Self::Danish,
        Self::Norwegian,
        Self::Swedish,
        Self::Finnish,
        Self::GavChinese,
        Self::DevReference,
    ];

    #[must_use]
    pub const fn all() -> &'static [Self] {
        Self::ALL
    }
}

